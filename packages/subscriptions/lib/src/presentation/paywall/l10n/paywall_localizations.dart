class _ErrorMessages {
  const _ErrorMessages({
    required this.configuration,
    required this.purchaseNotAllowed,
    required this.paymentPending,
    required this.paymentFailed,
    required this.alreadySubscribed,
    required this.subscriptionNotFound,
    required this.internalServer,
    required this.connectionTimeout,
    required this.insufficientPermissions,
    required this.packagesMissing,
    required this.unexpected,
  });

  final String configuration;
  final String purchaseNotAllowed;
  final String paymentPending;
  final String paymentFailed;
  final String alreadySubscribed;
  final String subscriptionNotFound;
  final String internalServer;
  final String connectionTimeout;
  final String insufficientPermissions;
  final String packagesMissing;
  final String unexpected;

  String forFailure(SubscriptionFailure f) => switch (f) {
    SubscriptionFailure.configuration => configuration,
    SubscriptionFailure.packagesMissing => packagesMissing,
    SubscriptionFailure.purchaseNotAllowed => purchaseNotAllowed,
    SubscriptionFailure.purchaseCancelled => '',
    SubscriptionFailure.paymentPending => paymentPending,
    SubscriptionFailure.paymentFailed => paymentFailed,
    SubscriptionFailure.alreadySubscribed => alreadySubscribed,
    SubscriptionFailure.subscriptionNotFound => subscriptionNotFound,
    SubscriptionFailure.internalServer => internalServer,
    SubscriptionFailure.connectionTimeout => connectionTimeout,
    SubscriptionFailure.insufficientPermissions => insufficientPermissions,
    SubscriptionFailure.unexpected => unexpected,
  };
}

